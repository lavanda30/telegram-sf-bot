async def start(update: Update, ctx: ContextTypes.DEFAULT_TYPE):
    total = sum(len(b["items"]) for b in BRANDS.values())
    text = (
        "🛍 *Прайс Штори та Тюль*\n\n"
        f"В базі: *{total} позицій* · {len(BRANDS)} брендів\n\n"
        "Оберіть бренд або скористайтесь пошуком:"
    )
    await update.message.reply_text(text, parse_mode="Markdown", reply_markup=build_main_keyboard())


async def on_callback(update: Update, ctx: ContextTypes.DEFAULT_TYPE):
    q = update.callback_query
    await q.answer()
    data = q.data

    if data == "main":
        total = sum(len(b["items"]) for b in BRANDS.values())
        text = (
            "🛍 *Прайс Штори та Тюль*\n\n"
            f"В базі: *{total} позицій* · {len(BRANDS)} брендів\n\n"
            "Оберіть бренд або скористайтесь пошуком:"
        )
        await q.edit_message_text(text, parse_mode="Markdown", reply_markup=build_main_keyboard())

    elif data.startswith("brand:") or data.startswith("page:"):
        parts = data.split(":")
        brand_name = parts[1]
        page = int(parts[2])
        brand = BRANDS.get(brand_name)
        if not brand:
            await q.edit_message_text("Бренд не знайдено")
            return
        items = brand["items"]
        text = build_brand_list_text(brand_name, items, page)
        kb = build_brand_keyboard(brand_name, items, page)
        await q.edit_message_text(text, parse_mode="Markdown", reply_markup=kb)

    elif data == "search":
        ctx.user_data["waiting_search"] = True
        await q.edit_message_text(
            "🔍 *Введіть назву або артикул* для пошуку:\n\n"
            "Наприклад: `HARMONY`, `блекаут`, `оксамит`, `Donna`, `8132`, `l205`, `лайк вайб`",
            parse_mode="Markdown"
        )


async def on_text(update: Update, ctx: ContextTypes.DEFAULT_TYPE):
    text = update.message.text.strip()

    # Гнучкий пошук: звичайний + нормалізований (без пробілів/тире/слешів)
    query = text.lower()
    query_norm = normalize_query(text)

    results = []
    seen = set()  # уникаємо дублів

    for brand_name, brand in BRANDS.items():
        for item in brand["items"]:
            matched = False
            for v in item:
                sv = str(v).lower()
                sv_norm = normalize_query(sv)
                if query in sv or (query_norm and query_norm in sv_norm):
                    matched = True
                    break
            if matched:
                key = (brand_name, str(item[0]))
                if key not in seen:
                    seen.add(key)
                    results.append((brand_name, item))

    ctx.user_data["waiting_search"] = False

    if not results:
        await update.message.reply_text(
            f"❌ По запиту *{text}* нічого не знайдено\n\n"
            "💡 Спробуйте без пробілів: `l205`, `anro`, `байрактар`",
            parse_mode="Markdown",
            reply_markup=build_main_keyboard()
        )
        return

    msg = f"🔍 Знайдено *{len(results)}* по «{text}»:\n\n"
    shown = results[:15]

    for brand_name, item in shown:
        brand = BRANDS[brand_name]
        tag = get_tag(item, brand_name)
        name = str(item[0])
        price_str = fmt_price(brand, item)
        extra_idx = brand.get("extra_idx")
        extra = ""
        if extra_idx and extra_idx < len(item):
            v = str(item[extra_idx])
            if v and v not in ("—", "", "В наявності", "РОЗПРОДАЖ"):
                extra = f" · _{v}_"
        msg += f"{tag} [{brand_name}] `{name}` — {price_str}{extra}\n"

    if len(results) > 15:
        msg += f"\n_...і ще {len(results)-15} результатів. Уточніть запит._"

    kb = InlineKeyboardMarkup([[
        InlineKeyboardButton("🔍 Новий пошук", callback_data="search"),
        InlineKeyboardButton("🏠 Головна", callback_data="main")
    ]])
    await update.message.reply_text(msg, parse_mode="Markdown", reply_markup=kb)


async def search_cmd(update: Update, ctx: ContextTypes.DEFAULT_TYPE):
    if ctx.args:
        update.message.text = " ".join(ctx.args)
        await on_text(update, ctx)
    else:
        ctx.user_data["waiting_search"] = True
        await update.message.reply_text(
            "🔍 Введіть назву або артикул для пошуку:\n"
            "Пошук гнучкий — `l205` = `L 205`, `лайк вайб` = `Лайк/Вайб`"
        )


