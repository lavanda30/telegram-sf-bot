def normalize_query(s: str) -> str:
    """Видаляє пробіли, тире, слеш, крапку — для гнучкого пошуку.
    Наприклад: 'l 205' → 'l205', 'лайк/вайб' → 'лайквайб'
    """
    return re.sub(r'[\s\-_/.]', '', str(s)).lower()


