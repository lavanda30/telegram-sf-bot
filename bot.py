def fmt_price(brand, item):
    pi = brand["price_idx"]
    price = item[pi] if pi < len(item) else None
    if price is None:
        return "—"
    if brand["usd"]:
        uah = round(float(price) * UAH_RATE)
        p_str = int(price) if float(price) == int(float(price)) else price
        return f"*{p_str}$* · ~{uah}грн"
    else:
        dp = price
        rp = item[pi - 1] if pi > 0 else None
        if rp is not None and rp != dp:
            return f"*{rp:.2f}* / *{dp:.2f}* грн"
        return f"*{dp:.2f}* грн" if dp else "—"


def get_tag(item, brand_name):
    for val in item[1:]:
        v = str(val).upper()
        if "РОЗПРОДАЖ" in v: return "🔴"
        if "НОВИНКА" in v: return "🟢"
        if "ЗНИЖЕННЯ" in v: return "🟡"
        if "ПІД ЗАМОВЛЕННЯ" in v or "ЗАМОВЛЕННЯ" in v: return "📦"
        if "ЗНЯТО" in v: return "⛔"
    return ""


def build_brand_list_text(brand_name, items, page):
    brand = BRANDS[brand_name]
    start = page * PAGE_SIZE
    end = min(start + PAGE_SIZE, len(items))
    total = len(items)
    extra_idx = brand.get("extra_idx")

    text = f"{brand['emoji']} *{brand_name}*\n"
    text += f"_{brand['note']}_\n"
    text += f"Показано {start+1}–{end} з {total}\n\n"

    for item in items[start:end]:
        tag = get_tag(item, brand_name)
        name = str(item[0])
        price_str = fmt_price(brand, item)
        extra = ""
        if extra_idx is not None and extra_idx < len(item):
            v = str(item[extra_idx])
            if v and v not in ("—", "", "В наявності", "РОЗПРОДАЖ"):
                extra = f"\n   _{v}_"
        text += f"{tag} `{name}` — {price_str}{extra}\n"

    return text


def build_brand_keyboard(brand_name, items, page):
    total_pages = (len(items) + PAGE_SIZE - 1) // PAGE_SIZE
    btns = []
    nav = []
    if page > 0:
        nav.append(InlineKeyboardButton("◀️ Назад", callback_data=f"page:{brand_name}:{page-1}"))
    if page < total_pages - 1:
        nav.append(InlineKeyboardButton("Далі ▶️", callback_data=f"page:{brand_name}:{page+1}"))
    if nav:
        btns.append(nav)
    btns.append([InlineKeyboardButton("🔍 Пошук", callback_data="search"),
                 InlineKeyboardButton("🏠 Головна", callback_data="main")])
    return InlineKeyboardMarkup(btns)


def build_main_keyboard():
    rows = []
    brand_list = list(BRANDS.keys())
    for i in range(0, len(brand_list), 2):
        row = []
        for brand in brand_list[i:i+2]:
            b = BRANDS[brand]
            row.append(InlineKeyboardButton(
                f"{b['emoji']} {brand}",
                callback_data=f"brand:{brand}:0"
            ))
        rows.append(row)
    rows.append([InlineKeyboardButton("🔍 Пошук по всіх брендах", callback_data="search")])
    return InlineKeyboardMarkup(rows)


